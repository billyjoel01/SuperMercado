    private void agregarProducto(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarProducto
        PanelProducto p = new PanelProducto();
        if (JOptionPane.showConfirmDialog(this, p, "Agregar nuevo producto", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
            productos.put(p.getCodigo(), new Producto(p.getCodigo(), Double.parseDouble(p.getPrecio())));
            productUtils.actualizarProductos(productos);
            JOptionPane.showMessageDialog(this, "Se agreg√≥ el producto", "Nuevo producto", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_agregarProducto

    private void actualizarProducto(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarProducto
        String codigo = JOptionPane.showInputDialog(this, "Introduce el codigo del producto", "Actualizar producto", JOptionPane.OK_CANCEL_OPTION);
        if (codigo != null && !codigo.isEmpty()) {
            if (productos.containsKey(codigo)) {
                Producto p = productos.get(codigo);
                PanelProducto panel = new PanelProducto(p.getCodigo(), p.getPrecio().toString());
                if (JOptionPane.showConfirmDialog(this, panel, "Actualizar producto", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
                    if (!p.getCodigo().equals(panel.getCodigo())) {
                        productos.remove(p.getCodigo());
                    }
                    productos.put(panel.getCodigo(), new Producto(panel.getCodigo(), Double.parseDouble(panel.getPrecio())));
                    productUtils.actualizarProductos(productos);
                    JOptionPane.showMessageDialog(this, "Se actualiz√≥ el producto", "Producto actualizado", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Producto no encontrado", "Actualizar producto", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_actualizarProducto

    private void eliminarProducto(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarProducto
        String codigo = JOptionPane.showInputDialog(this, "Introduce el codigo del producto", "Eliminar producto", JOptionPane.OK_CANCEL_OPTION);
        if (codigo != null && !codigo.isEmpty()) {
            if (productos.containsKey(codigo)) {
                productos.remove(codigo);
                productUtils.actualizarProductos(productos);
                JOptionPane.showMessageDialog(this, "Se elimin√≥ el producto", "Eliminar producto", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Producto no encontrado", "Elimninar producto", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_eliminarProducto

    private void incrementarPrecios(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_incrementarPrecios
        productos.values().forEach(p -> {
            p.setPrecio(redondear(p.getPrecio() * 1.05));
        });
        productUtils.actualizarProductos(productos);
        JOptionPane.showMessageDialog(this, "Se increment√≥ el precio de los productos en 5%", "Incrementar precios", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_incrementarPrecios

    private void btnCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarritoActionPerformed
        do {
            PanelDetalleFactura pdf = new PanelDetalleFactura(productos);
            if (JOptionPane.showConfirmDialog(this, pdf, "Agregar a la venta", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
                Detalle d = pdf.getDetalle();
                if (d != null) {
                    detalles.add(d);
                } else {
                    break;
                }
            } else {
                break;
            }
        } while (true);

        Double subtotal = 0.00;
        Double iva;
        Double total;

        String[] cols = new String[]{"Codigo", "Cantidad", "Precio", "Total"};
        String[][] data = new String[detalles.size()][4];
        for (int i = 0; i < data.length; i++) {
            subtotal += detalles.get(i).getTotal();
            data[i] = new String[]{
                detalles.get(i).getCodigo(),
                detalles.get(i).getCantidad().toString(),
                detalles.get(i).getPrecio().toString(),
                detalles.get(i).getTotal().toString()
            };
        }
        DefaultTableModel dtm = new DefaultTableModel(data, cols);
        tblDetalles.setModel(dtm);
        tblDetalles.repaint();

        iva = subtotal * IVA;
        total = subtotal + iva;

        lblSubtotal.setText(String.format("%.2f", subtotal));
        lblIva.setText(String.format("%.2f", iva));
        lblTotal.setText(String.format("%.2f", total));
    }//GEN-LAST:event_btnCarritoActionPerformed

