    /**
     * Devuelve el producto
     *
     * @param codigo
     * @return
     */
    private void buscarProducto() {
        if (!txtCodigo.getText().isEmpty()) {
            p = productos.get(txtCodigo.getText());
            if (p != null) {
                txtCantidad.setText("1");
                lblPrecio.setText(p.getPrecio().toString());
                lblTotal.setText(p.getPrecio().toString());
            } else {
                txtCantidad.setText("0");
            }
        }
    }

    public Detalle getDetalle() {
        if (p == null) {
            return null;
        } else {
            return new Detalle(p.getCodigo(), p.getPrecio(), Integer.parseInt(txtCantidad.getText()), Double.parseDouble(lblTotal.getText()));
        }
    }

