    private void btnPlusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlusActionPerformed
        if (txtCantidad.getText().isEmpty()) {
            txtCantidad.setText("0");
        } else {
            Integer cantidad = Integer.parseInt(txtCantidad.getText()) + 1;
            txtCantidad.setText(cantidad.toString());
            lblTotal.setText(String.format("%.2f", (p.getPrecio() * cantidad)));
        }
    }//GEN-LAST:event_btnPlusActionPerformed

    private void btnMinusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMinusActionPerformed
        if (txtCantidad.getText().isEmpty()) {
            txtCantidad.setText("0");
        } else {
            Integer cantidad = Integer.parseInt(txtCantidad.getText());
            cantidad = cantidad <= 0 ? 0 : cantidad - 1;
            txtCantidad.setText(cantidad.toString());
            lblTotal.setText(String.format("%.2f", (p.getPrecio() * cantidad)));
        }
    }//GEN-LAST:event_btnMinusActionPerformed

